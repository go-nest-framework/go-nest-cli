// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "gn",
	Short: "CLI for generating Go Nest applications",
	Long: `Go Nest CLI is a tool designed to simplify and automate the setup and development of 
modular, clean architecture-based Go applications inspired by NestJS. 
With this CLI, you can quickly scaffold new projects, create modules, services, and much more, 
all following a well-structured architecture.`,
	// Uncomment the following line if your bare application
	// has an action associated with it:
	// Run: func(cmd *cobra.Command, args []string) { },
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

